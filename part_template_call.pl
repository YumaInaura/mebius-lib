);

# テンプレートをゲット
($line) .= &get_templatetext;
$line .= $redcard;

# テンプレートの調整
$line =~ s/%/%25/g;
$line =~ s/\[br\]/\r/g;
$line =~ s/>>/&gt;/g;
$line =~ s/\n//g;
$line =~ s/\r/\\n/g;

return($line);

}

